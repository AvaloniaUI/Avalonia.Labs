namespace Avalonia.Labs.Controls;

/// <summary>
/// Specifies the direction to open swipe items
/// </summary>
public enum OpenSwipeItem
{
    /// <summary>
    /// Opens from the left to reveal swipe items
    /// </summary>
    LeftItems,

    /// <summary>
    /// Opens from the top to reveal swipe items
    /// </summary>
    TopItems,

    /// <summary>
    /// Opens from the right to reveal swipe items
    /// </summary>
    RightItems,

    /// <summary>
    /// Opens from the bottom to reveal swipe items
    /// </summary>
    BottomItems
}
