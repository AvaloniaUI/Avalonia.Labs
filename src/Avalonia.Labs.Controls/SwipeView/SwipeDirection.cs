namespace Avalonia.Labs.Controls;

/// <summary>
/// Defines the direction of a swipe gesture
/// </summary>
public enum SwipeDirection
{
    /// <summary>
    /// Swipe gesture is moving to the right
    /// </summary>
    Right,

    /// <summary>
    /// Swipe gesture is moving to the left
    /// </summary>
    Left,

    /// <summary>
    /// Swipe gesture is moving upward
    /// </summary>
    Up,

    /// <summary>
    /// Swipe gesture is moving downward
    /// </summary>
    Down
}
